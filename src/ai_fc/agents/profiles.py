# 에이전트 수 → 프로필 구성 (P0 표준: 종합 + 데블스)
PROFILE_SETS = {
    2: ["general", "devil"],
    4: ["fundamental", "macro", "flow", "devil"],
}


def get_profile(name: str, words: int = 900) -> str:
    """프로필 시스템 프롬프트 — 분량 지시만 티어별 치환 (v3 WS-B lite).

    임무 텍스트·데블스 강제·출처 규칙은 불변 — 경량화는 분량(900→450)뿐.
    """
    prompt = PROFILES[name]
    if words != 900:
        prompt = prompt.replace("900단어 이내", f"{words}단어 이내")
    return prompt
